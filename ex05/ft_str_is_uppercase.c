#include <stdio.h>

int	ft_str_is_uppercase(char *str)
{
	int	i;
	int	n;

	i = 0;
	n = 1;
	if (str[i] == '\0')
		return (1);
	while (str[i] != '\0')
	{
		if (str[i] < 'A' || str[i] > 'Z')
			n = 0;
		i++;
	}
	return (n);
}

